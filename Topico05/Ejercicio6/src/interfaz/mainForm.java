    private void btn_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularActionPerformed
        if(inputNombre.getText() != "" && inputHora.getText() != "" && inputRetencion.getText() != "" && inputSalario.getText() != ""){
           String nombre = inputNombre.getText();
           float horas = Integer.parseInt(inputHora.getText());
           float salarioPorHoras = Integer.parseInt(inputSalario.getText());
           float retencion = Integer.parseInt(inputRetencion.getText());
           
           float sueldoBruto = salarioPorHoras * horas;
           retencion = (retencion * sueldoBruto) / 100;
           float sueldoNeto = sueldoBruto - retencion;
           
           Object datos[]={nombre,sueldoBruto,retencion,sueldoNeto};
           md.addRow(datos);
           inputNombre.setText("");
           inputHora.setText("");
           inputRetencion.setText("");
           inputSalario.setText("");
        }else{
            
        }
    }//GEN-LAST:event_btn_calcularActionPerformed

    private void btn_limpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_limpiarActionPerformed
           int filas = md.getRowCount();
           
            for(int i = 0; i < filas; i++){
                md.removeRow(0);
           }
           inputNombre.setText("");
           inputHora.setText("");
           inputRetencion.setText("");
           inputSalario.setText("");
    }//GEN-LAST:event_btn_limpiarActionPerformed

